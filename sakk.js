var kiraly = document.getElementById("kiraly_szoveg");
kiraly.style.visibility = 'hidden';

function kiraly_kep(){
    document.getElementById("kiraly_szoveg")
        if (kiraly_szoveg.style.visibility == "hidden"){
            kiraly_szoveg.style.visibility = "visible"
        }else{
            kiraly_szoveg.style.visibility = "hidden"
        }

}
